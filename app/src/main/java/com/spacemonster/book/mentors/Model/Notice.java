package com.spacemonster.book.mentors.Model;

public class Notice {
    String list1;
    String list2;

    public Notice(String list1, String list2) {
        this.list1 = list1;
        this.list2 = list2;
    }

    public String getList1() {
        return list1;
    }

    public String getList2() {
        return list2;
    }

    public void setList1(String list1) {
        this.list1 = list1;
    }

    public void setList2(String list2) {
        this.list2 = list2;
    }
}
